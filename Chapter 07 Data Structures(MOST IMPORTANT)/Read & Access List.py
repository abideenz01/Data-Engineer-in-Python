#Accessing list
letters=['a','b','c','d','e']
print(letters[0:2])
print(letters[-2])
print(letters[2])
# Accessing Matrix
matrix=[['a','b','c'],
        ['d','e','f'],
        ['g','h','i']]
# whole matrix
print(matrix)
#single row
print(matrix[-1])
#one element
print(matrix[-1][-1])
print(matrix[0][0])
#two rows
print(matrix[0:-1])
#Slicing
letters=['a','b','c','d','e']
#slicing last 2 characters
print(letters[3:])
#slicing first 2 characters
print(letters[:2])
#slicing whole list 
print(letters[:])
#Slicing Matrix
matrix=[['a','b','c'],
        ['d','e','f'],
        ['g','h','i']]
#slicing first 2 elements of last row
print(matrix[-1][0:2])
#slicing 3rd column
print(matrix[:][2])





                 